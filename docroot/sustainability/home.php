        <p>For decades, organizations across the country have sought to increase recycling rates by encouraging people to put more items in their recycling bins. Unfortunately, this practice benefits no one if those items are not actually recyclable.</p>
        <p>It is time to pause and remember why we recycle. Many have come to believe, simplistically, that the benefit of recycling comes from keeping stuff out of landfills. This ignores the crucial fact that environmental impact of landfilling is negligible compared to the extraction and transportation activities that produced them. <em><strong>The great promise of recycling</strong> is not that we can eliminate landfills, but that we can eliminate mining and deforestation.</em></p>
        <p>A myopic focus on "diversion" has distracted everyone from the daunting but essential work of building a resource-cycling economy in which today's discards consistently become the raw material for tomorrow's new products. With this report, we hope to reset the conversation, reminding everyone that recycling is far from simple, and it doesn't begin or end at the recycling bin. To endure, recycling solutions must be practical, they must be environmentally, economically, and socially-sustainable, and they must be embedded in a resource renewal economy that transforms the supply chains of everything we purchase and consume.</p>
