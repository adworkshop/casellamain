          <ul class="content">
            <li>Global trends in population and economic development are placing mounting pressure on natural resource supply chains. For both environmental and economic reasons, the world needs high quality recycled commodities to minimize our reliance on virgin resources.</li>
            <li>The mix of material in the disposal stream has shifted. We are seeing less paper and a wider variety of plastic packaging in the recycling stream. In general, we are seeing a larger ratio of industrial material entering the disposal stream, and less municipal material due to local recovery efforts.</li>
            <li>The economic and environmental viability of the Waste-to-Energy (WTE) incineration model is coming into question, leaving communities to decide whether to enter long-term agreements with WTE suppliers, or pursue alternatives.</li>
            <li>State-level decision-makers are passing new rules in hopes of creating environmental benefits, economic efficiencies, and green jobs. Vermont's Act 148 Universal Recycling Law and Massachusetts' new commercial organics ban are examples.</li>
            <li>Customers want more than just reliable, safe, and compliant solid waste service; they are looking for partners to help them meet recycling goals and a new professional services model that includes access to data, recycling options for more of their waste streams, and low carbon disposal options.</li>
            <li>The most progressive customers are taking a sophisticated approach that looks beyond the solid waste budget line-item to recognize and invest in the full value creation possibilities of holistic resource management.</li>
          </ul>
          <p>Against this evolving backdrop, we have gravitated toward a strategic approach that emphasizes:</p>
          <ol class="content">
            <li>Listening to customers to deeply understand their service needs.</li>
            <li>Drawing upon our operational expertise and knowledge to solve pressing problems.</li>
            <li>Applying our expertise in commodity markets to help customers capture value from their waste.</li>
            <li>Deploying solutions that are flexible and responsive to customer needs.</li>
          </ol>
          <p>The approach is customer- rather than technology-centric. Instead of a one-size-fits-all, build-it-and-they-will-come approach, it is adaptive to evolving local needs. The resulting solutions and expertise leave us uniquely positioned to deliver shared environmental and economic value to our customers and communities, which in turn positions our company to thrive in the post-disposal, resource renewal economy.</p>
          
