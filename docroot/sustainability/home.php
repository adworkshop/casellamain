            <h3>Resource Renewal</h3>
          </div>
          <p>We envision a resource renewal future in which nothing would be wasted, resource extraction would be all but eliminated, and human society could come into alignment with natural systems.</p>
          <p>Achieving resource renewal will not be easy. Roughly 50% of the waste from U.S. homes and businesses consists of materials that could be recycled or composted using technology available today. We need to get those materials into the right bins; but that will get us only halfway to our goal. The rest of the journey will require tougher steps: stop buying stuff that isn’t recyclable, engage suppliers to redesign for recyclability, purchase products made from recovered materials, and invest in the next wave of resource recovery infrastructure.</p>
          <p>To advance resource renewal, we are helping our customers set smart goals and providing them with a growing suite of Resource Solutions to help them achieve those goals. (Section 1 defines and describes our Resource Solutions). Still, we know that success will require much more. Our progress will be helped or hindered by systemic factors such as markets, policy, technology, culture, and infrastructure. Only by aligning all of these stakeholders and forces will society achieve resource renewal. This "puzzle" is adjacent.</p>
          <h4>It Begins with Smart Resource Renewal Goals</h4>
          <p>We have thousands of customers, including manufacturers, colleges, schools, hospitals, small businesses, municipalities, and households, who are actively working to reduce their waste. The most dedicated among them are working with us to set smart resource renewal goals that are data-driven and achievable. We provide these customers with resource renewal plans that go far beyond improved sorting to also include source reduction, procurement policies, supply chain engagement, and more.</p>
          <p>With our Core Values as our guide, and decades-long relationships to build upon, we have been fortunate to forge many strong partnerships to advance resource renewal in our region.</p>
          <h4>Innovative &amp; Reliable Resource Solutions</h4>
          <p>As one of the leading resource management companies in the country, we are extremely proud of our high-quality environmental services, which we call resource solutions: recycling, organics, collection, energy, and landfills. With over 2,000 employees, 100+ facilities, 500+ front-line vehicles, and over 40 years of experience, we collect, aggregate, sort, process, market, and manage our customers’ discards every day.</p>
          <p>Every day, we apply our experience and our expertise to enhance our  resource solutions and to imagine and develop new ones that propel us into the future. We take an approach of disciplined innovation, seeking the ideas, models, and technologies that fit into a holistic resource management infrastructure for the customers we serve. One of our most exciting areas of investment today is in the development of our people and their resource renewal expertise. A growing number of customers value us not only as providers of the tangible services above, but also as providers of the knowledge and data that help them achieve their goals. Thus, we are rapidly enhancing our professional services such as data reporting, on-site management, waste reduction, life cycle assessment, strategic sourcing and logistics, supply chain analysis, and more. These skills will be fundamental to achieving our customers' goals as well as the broader resource renewal vision.</p>
          <h4>Systemic Change in Markets, Policy, Technology, Culture, &amp; Infrastructure</h4>
          <p>With the strong partnerships, smart goals, and resource renewal expertise described above, we will continue to drive progress toward the vision of resource renewal. However, we know that even the best programs eventually come up against the law of diminishing returns: early projects save money, then some projects even, but eventually we come up against waste streams that are too costly to prevent or recover. At this point, bigger change is required.</p>
          <p>Only deep systemic change will drive solutions for the hardest-to-recycle materials, or prevent them from being generated in the first place. Markets and culture need to reward waste reduction and diversion. New technology and infrastructure need to enable diversion of more materials. Smart policy will sometimes need to nudge things along.</p>
