          <h3>Shared Responsibility for Social Good</h3>
          <p>We believe that the services we provide and the investments we make in managing and renewing resources must be economically sustainable as well as environmentally sustainable. One cannot exist without the other.</p>
          <p>We also believe that recycling is part of a larger and more crucial set of challenges facing society and the planet. We are - we believe - entering an era of constraints and limits in nearly all areas of human activity, including energy, climate, and natural resources.</p>
          <p>In this belief, we are optimists. These challenges represent significant opportunities for innovation, creativity, economic growth and security, environmental stewardship, and quality of life for future generations. These challenges also demand major shifts in thinking, particularly around how and why we shape public policy, how and why we pay for resource renewal, and how and why we place a value on it - for ourselves, our children, and our society.</p>
