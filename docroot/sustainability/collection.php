      <p>Sustainable resource management would not be possible without safe and reliable collection service. We operate a network of refuse collection fleets extending from western New York through Northern Maine, which includes over 700 collection vehicles.</p>
      <p>Our fleet of on- and off-road vehicles consumes nearly 6 million gallons per year of diesel fuel, which is expensive and produces emissions. We therefore have a strong environmental and financial incentive to reduce our dependence on diesel by running a more efficient vehicle fleet. Our improvements for the coming years will include modernizing our fleet and using alternative fuels such as CNG and biodiesel in select markets.</p>
      <p>Across the company, we continue to modernize our fleet by retiring older vehicles and replacing them with newer, more efficient vehicles. These improvements will increase our overall operating efficiencies, reduce our emissions, and provide our operators with better tools to do their work.</p>
      <p>We have also begun to run a portion of our fleet on compressed natural gas (CNG), which has a lower carbon footprint, emits less local air pollution, and runs more quietly than diesel. Today, we are running 35 CNG-fueled refuse and recycling vehicles out of our divisions in Burlington, VT, Fort Edward, NY, Geneva, NY, and Horseheads, NY. In 2013, these vehicles allowed us to replace nearly 400,000 gallons of diesel fuel with CNG.</p>
      <p>In Portland, ME, our customers help us fuel our trucks. Here's how it works: several of our restaurant customers in Portland set aside their used vegetable oil for recycling. Maine Standard Biofuels pays them for the oil, which they collect and refine into biodiesel. We then buy and blend the fuel into our tanks to replace a portion of our diesel usage. Our Portland fleet runs on 20% biodiesel, and saved about 30,000 gallons of regular diesel last year.</p>
      <p>In addition to these measures, we are conducting demonstration tests to evaluate the cutting edge new technologies of the future. One such demonstration will be a hydraulic hybrid launch truck, which is estimated to consume 44% less diesel than current 2010 diesel technology. Another demonstration project will assess a fuel saving technology that manages engine power to optimize fuel efficiency and performance, with fuel economy benefits of up to 10%.</p>
