      <img src="img/collection/collection-2.jpg" alt="" class="responsive-img">
      <p><strong><em>CNG:</em></strong> Our fleet includes 37 refuse and recycling trucks that run on compressed natural gas (CNG), which has a lower carbon footprint and cleaner emission profile than diesel fuel. Since we bought our first CNG truck in 2010, we have displaced over 2 million gallons of diesel fuel with cleaner burning CNG.</p>
      <p>Most of our CNG trucks have 9-liter engines. Their limited power and excessive maintenance costs have prevented wide-scale roll-out. We have begun trialling trucks with 12-liter engines. Preliminary results suggest that they may meet our performance needs and enable us to grow our CNG fleet in the coming years.</p>
      <p><strong><em>Biodiesel:</em></strong> We have also had some success with biodiesel Our hauling division in Portland, Maine runs on a blend of locally-produced biodiesel. In 2017, the Portland fleet saved over 18,000 gallons of diesel fuel by using biodiesel.</p> 
      <p><strong><em>Electric Vehicles:</em></strong> Over the years we have experimented with electric vehicles. Because we operate in rural areas, we need vehicles that can cover long distances between fueling or charging. This has been an important limiting factor for electric vehicles in our fleet, but we believe the technology is improving and we are working with our vendors to conduct additional pilots this year.</p>
      <h2>Routing Efficiency</h2>
       <img src="img/collection/collection-3.jpg" alt="" class="responsive-img">
