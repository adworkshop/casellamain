                <p>Our goal with recycling is to produce high quality materials that manufacturers actively seek out for their products. We take pride in the quality of our recycled material, and to protect it, we are willing to invest considerable time and energy. This is an essential component of building a thriving recycling industry.</p>
                <p>One challenge that all recycling facility operators face is the presence of residue, or non-recyclable materials, mixed in with the inbound recyclables. Residue can diminish the quality of the recycling stream, impede sorting processes, increase operating costs, and potentially damage processing equipment or put facility employees at risk. Residue occurs when generators don't know what belongs in their recycling bin or don't appreciate the importance of proper sorting.</p>
                <p>We have always worked to keep our residue rates low, by educating our customers when they make the switch to Zero-Sort Recycling and providing them with educational handouts and signage. This year we are enhancing this effort by establishing an open dialogue to help customers understand the importance of good sorting. To learn what materials are recyclable in your area, please visit our website, <a href="https://www.casella.com/" title="visit casella.com">casella.com.</a></p>
