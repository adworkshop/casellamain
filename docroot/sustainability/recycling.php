            <p><strong>Paper &amp; Cardboard</strong> make up most of the recycling mix.Cardboard has increased as more consumers shop online and receive boxes by mail. Paper has declined as customers receive their media digitally rather than in the form of newspapers and magazines. We make processing equipment investments to accommodate these changes.
            <p><strong>Glass</strong> is a growing percentage of the recycling mix. This strains recycling facilities because glass is expensive to process and markets are very limited. Some municipalities have begun to exclude glass from their recycling mix because of these challenges. We are exploring several ways to make glass recycling economically viable.</p>
            <p class="rec-highlight"><i class="fas fa-exclamation-triangle"></i><strong>Trash</strong> in the recycling mix has reached alarming levels, due to confusion and carelessness about what belongs in the recycling bin. Our blended average has reached 9%, and individual loads come to us with contamination levels as high as 25% or even 50%! The impacts include risks to employee safety, damage to equipment, facility shutdowns, degradation of recyclable material, increases in disposal costs, and an overall decline in the efficiency and effectiveness of recycling systems. Coupled with recent market disruptions, the entire industry is grappling with this threat. We are working with our customers and partners to improve education and outreach.</p>
            <p><strong>Plastic &amp; Metal</strong> are the high-value commodities in the recycling mix. Their overall presence (as a percentage of total weight) has not changed significantly. The presence of antiquated container deposit systems in some states continues to siphon valuable aluminum and polyethylene out of the municipal recycling stream, harming the economic equation that enables recycling of lower-value commodities like other plastics, mixed paper, and glass.</p>
            <p>As the recycling mix changes, we must continuously innovate, upgrade our infrastructure, update our education programs, and help policymakers rethink legislative models. We must also be disciplined in deploying a business model that can evolve along with the recycling stream to keep recycling programs strong.</p>
