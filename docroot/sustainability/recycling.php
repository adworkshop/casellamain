    <div class="globalWidth">
      <div class="recycleSustainableContainer threeColumnContentContainer">
        <p>Recycling is a cornerstone of sustainability. It benefits the planet twice: first by reducing waste disposal and second by alleviating pressure on natural resources. When someone wants to improve the environmental impact of their business or household, recycling is often the first step they take.</p>
        <p>Still, society is not recycling enough. Diversion rates in the United States have stagnated, and disposal audits consistently show large percentages of recyclables in the trash. Some blame society's laziness or apathy, but the problem is not solely cultural; it is also economic. The business model of recycling remains overly reliant on volatile commodity markets and is sorely in need of transformation.</p>
        <div class="recycleSustainableTitle">Recycling is Not Free</div>
        <p>At first glance, recycling has an enticing business model, in which recyclers get paid twice for the material they collect. Generators pay to have stuff taken away and then manufacturers pay to purchase
        that same stuff. Unfortunately, however, these revenues don't reliably outweigh the significant costs of recyclables collection and processing infrastructure.</p>
        <p>The downside of the recycling business model is that it is exposed to global commodity markets, which are volatile. Several times in recent history, recycling revenues have declined below the levels required to recoup collection and processing costs and investments. In short, recycling periodically becomes unprofitable and does not deliver an appropriate return. This situation deters much-needed further investment in recycling infrastructure.</p>
        <p>The fundamental - and ironic - problem is that, despite the high social and environmental value that we place on recycling, society has saddled it with the unrealistic expectation that it should be free. Recycling is not free. Collecting and processing recyclables requires significant up-front investments and ongoing operational costs. It requires a vast new physical infrastructure. No one expected highway networks or indoor plumbing to be free; why do we think differently about recycling? The unrealistic expectation that recycling should be free effectively stacks the deck against recycling, and it is time for society to address this problem head on.</p>
        <div class="recycleSustainableTitle">Entering a New Era</div>
        <p>We believe that the "recycling is free" myth has prevented society from fully unlocking the power of markets to support a thriving and resilient recycling industry. The six strategies presented below will begin to change this. Over the coming months and years, Casella will work with its many partners to imagine and build a new recycling industry that is both environmentally and economically sustainable.</p>
