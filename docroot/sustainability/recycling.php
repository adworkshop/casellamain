          <p>In 2017/2018, China rocked global recycling markets with a swift series of policy changes. The impacts - a 90% drop in market value for mixed paper and a 63% drop in the value of the traditional recycling stream - have left recyclers reeling.</p>
          <p>It is no secret that a significant portion of the products in the global economy are manufactured in China. So it comes as no surprise that China became the top importer of recycled plastic and paper, the raw materials for all those products and their packaging. In recent years, Chinese manufacturers developed such a strong appetite for material, that they accepted it even with high contamination.</p>
          <p>This lax approach changed abruptly early this year, when China banned the import of 24 types of recyclable materials and established exceedingly stringent quality standards for other recyclables. Even recyclers with high quality standards - including Casella - are now struggling to sell their outputs.</p>
          <p>To date, China's actions have had negative consequences. With a shortage of recycled feedstock, demand for virgin wood pulp from timber harvesting has risen. Meanwhile, many recycling programs throughout the western world have been forced to curtail their recycling programs or dispose of recyclables.</p>
