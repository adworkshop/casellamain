                <div class="indMythContentTxt">
                  <p>EPA's waste hierarchy reminds us that source reduction and recycling should always be our highest priorities. Facilities that pursue Zero Landfill through incineration can undermine recycling and related diversion opportunities. For example, Waste-to-Energy facilities have no incentive to find solutions for hard-to-recycle plastics, because plastic is a key ingredient in incineration power production. But plastic is a petroleumbased product; when combusted for energy, it is a fossil fuel. From a climate perspective, there is no question: true recycling means getting those carbon molecules into new products, not combusting them to raise carbon dioxide concentrations in our atmosphere.</p>
                  <p>Zero Waste challenges us to keep working until all of the materials we manage are out of the disposal stream. As a society, we could settle for Zero Landfill, but if we're serious about protecting the climate, closing resource loops, and achieving true sustainability, we have to set the higher standard: Zero Waste.</p>
