                <div class="indMythContentTxt">
                  <p>The EPA used to advocate a waste hierarchy that ranked waste-to-energy incineration above landfilling. This dates back to the late 1980s when landfill gas collection was uncommon, and few landfills captured landfill gas for energy. In 2012, based on modern operating practices and data, EPA revised its waste hierarchy to the one shown in Figure 3<sup>1</sup>. The hierarchy no longer places wasteto- energy above landfilling, but instead lists "Energy Recovery" above "Treatment &amp; Disposal". In this manner, a landfill with energy recovery is ranked in the same tier as an incinerator with energy recovery.</p>
