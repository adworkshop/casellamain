            <div class="organics-caseStudy__title">Responsible Nutrient Management </div>
            <div class="organics-caseStudy__teaser">Anyone with a garden knows that plants require soil nutrients - such as phosphorus, nitrogen, potassium, calcium, magnesium, and more - to grow. However, excess nutrients can run off into rivers and lakes and lead to problematic algal blooms, choking weeds, and habitat degradation, which can in turn impair public water supplies, recreational resources, and wildlife biodiversity.</div>
