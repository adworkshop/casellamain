            <div class="organics-caseStudy__content--text">
              <p>States in the northeast are developing new regulatory programs designed to limit nutrient (phosphorus) runoff from sources such as agricultural lands and suburban runoff. This growing focus on nutrient runoff is one of the most significant emerging factors influencing our ability to beneficially use organic residuals in northeastern watersheds.</p>
              <p>Soil products made from organic residuals could play a significant role in helping to reduce phosphorus runoff. This is because the products contain carbon, nitrogen, and micronutrients, which help to enhance soil structure and support plant growth. Unfortunately, because these products do contain phosphorus, overly simplistic nutrient management rules could deter their use.</p>
              <p>We believe that recycled organics can and should continue to play a valuable role in building our region’s cropland and landscapes, and in protecting our watersheds. Casella’s Nutrient Management Planners are working with decision-makers to develop and advance best management practices, based in sound soil science, that will allow and encourage the beneficial use of recycled organics.</p>
