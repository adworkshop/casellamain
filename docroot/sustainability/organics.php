                  <h2>Making Animal Bedding in Maine</h2>
                  <p>During the summer of 2016, Casella Organics visited the Verso Paper Mill in Jay, Maine with a proposal to recycle the facility's short paper fiber (SPF) into a bedding product for dairy cows. Verso embraced the idea, and the two companies collaborated closely to make it happen.</p>
                  <p>Success took time, but in 2017 the mill adjusted its operations to separate primary and secondary SPF and took a series of steps to meet regulatory quality standards. The extra effort has proven worthwhile.</p>
                  <p>Today at our Hawk Ridge Compost Facility, we combine and blend Verso's primary short paper fiber with select types of wood ash to produce a quality bedding called <strong>Fiberbed&reg;</strong>. The product naturally excels at absorbing liquids, and a Fiberbed study at the University of Maine found reduced instances of mastitis-causing bacteria. As an added benefit, when a farm uses a load per week of Fiberbed and spreads the resulting bedded manure on their fields, they receive approximately $50,000 of lime value.</p>
                  <p>Through innovation, teamwork, and careful management, we're now combining two "waste" materials to produce a valuable agricultural product. Last year, 25 dairy farms throughout the region benefited from the new Fiberbed production.</p>
